function createData(
  name: string,
  calories: number,
  fat: number,
  carbs: number,
  protein: number,
) {
  return { name, calories, fat, carbs, protein };
}



const rows = [
  createData('Carlos', 1, -2, 0 , 5.3),
  createData('Allen', 3, 69, 0, 5.0),
  createData('Joey', 200, 15, 0, 6.0),
];

export default function BasicTable() {
  return (
    <TableContainer component={Paper}>
      <Table sx={{ minWidth: 650 }} aria-label="simple table">
        <TableHead>
          <TableRow>
            <TableCell>Goon (100g serving)</TableCell>
            <TableCell align="right">Bitches</TableCell>
            <TableCell align="right">Aura&nbsp;(g)</TableCell>
            <TableCell align="right">Fantasy Championships&nbsp;(g)</TableCell>
            <TableCell align="right">40 yard time&nbsp;(g)</TableCell>
          </TableRow>
        </TableHead>
        <TableBody>
          {rows.map((row) => (
            <TableRow
              key={row.name}
              sx={{ '&:last-child td, &:last-child th': { border: 0 } }}
            >
              <TableCell component="th" scope="row">
                {row.name}
              </TableCell>
              <TableCell align="right">{row.calories}</TableCell>
              <TableCell align="right">{row.fat}</TableCell>
              <TableCell align="right">{row.carbs}</TableCell>
              <TableCell align="right">{row.protein}</TableCell>
            </TableRow>
          ))}
        </TableBody>
      </Table>
    </TableContainer>
  );
}


